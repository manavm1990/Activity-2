// Comparison Operators - always evaluate to a boolean value
const total = 30;
const otherTotal = 40;

console.log(3 < 2); // false
console.log(total <= 3); // false

console.log(total >= 30); // true

// '===' is a strict equality operator
// '===' checks for equality in value and type
console.log(total === otherTotal); // false
console.log("30" === 30); // false
